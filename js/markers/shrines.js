function shrineUI(object) {

    let markerHtml = `
    <div>
        <h5>
            <a href="${location.protocol}//${location.host}${location.pathname}?z=${window.map.getMaxZoom()}&x=${object.coords[0]}&y=${object.coords[1]}">
                ${object.name}
            </a>
        </h5>
        <p>
            Level: ${object.level}</br>
            Location: ${object.coords[0]},${object.coords[1]}
        </p>
        <p>${object.text}</p>
        </h5>
    </div>
    `
    return markerHtml;
}

function getShrines(level = null) {
    var icon = L.icon({
        iconUrl: './assets/marker/shrine_r.png',
        iconSize: [25, 25],
    });

    let layerGroupArray = [];
    for (object in shrines) {
        if (shrines[object].level == null || shrines[object].level == level) {
            layerGroupArray.push(L.marker(window.rc.unproject(shrines[object].coords), { icon: icon }).bindPopup(shrineUI(shrines[object])));
        }
    }

    return L.layerGroup(
        layerGroupArray
    );
}


